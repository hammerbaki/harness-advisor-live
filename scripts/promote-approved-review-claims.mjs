import { createHash } from "node:crypto";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = resolve(fileURLToPath(new URL("..", import.meta.url)));
const now = new Date().toISOString();
const approvalPath =
  process.env.CLAIM_REVIEW_APPROVAL_IN ?? "raw/manifests/claim-review-approval-record.json";
const outputPath =
  process.env.REVIEW_APPROVED_PROMOTION_OUT ?? "raw/manifests/review-approved-runtime-promotion.json";
const docPath =
  process.env.REVIEW_APPROVED_PROMOTION_DOC ?? "docs/85_review_approved_runtime_claims.md";

const approval = await readJson(approvalPath);
const policy = await readJson("configs/source-selection-policy.json");
const groupsConfig = await readJson("configs/groups.json");
const knownPolicyRuleIds = new Set([
  ...(policy.scopePrinciples ?? []).map((rule) => rule.id),
  ...(policy.selectionRules ?? []).map((rule) => rule.id)
]);
const groupsById = new Map((groupsConfig.groups ?? []).map((group) => [group.id, group]));
const claimPrefixByGroup = {
  samsung: "samsung",
  sk: "sk",
  "hyundai-motor": "hyundai",
  lg: "lg",
  hanwha: "hanwha"
};

const approvedRecords = approval.records ?? [];
if (approvedRecords.length === 0) throw new Error(`${approvalPath} has no approved records`);

const promotedByGroup = [];
for (const groupId of unique(approvedRecords.map((record) => record.groupId))) {
  const group = groupsById.get(groupId);
  if (!group) throw new Error(`Unknown groupId in approval record: ${groupId}`);
  const manifestPath = `raw/manifests/${groupId}.source-backed-claims.json`;
  const manifest = await readJson(manifestPath);
  const preservedRecords = (manifest.records ?? []).filter(
    (record) => record.paperUseLevel !== "source-backed-review-approved-claim"
  );
  const nextSequence = nextClaimSequence(preservedRecords, claimPrefixByGroup[groupId] ?? groupId);
  let sequence = nextSequence;
  const groupApprovalRecords = approvedRecords.filter((record) => record.groupId === groupId);
  const promotedRecords = [];

  for (const record of groupApprovalRecords) {
    promotedRecords.push(await buildPromotedRecord(record, group, sequence));
    sequence += 1;
  }

  const records = [...preservedRecords, ...promotedRecords];
  const updatedManifest = {
    ...manifest,
    generatedAt: now,
    selectionPolicyVersion: manifest.selectionPolicyVersion ?? policy.policyVersion,
    reviewApprovedPromotion: {
      sourceApprovalManifest: approvalPath,
      promotedAt: now,
      promotedRecords: promotedRecords.length,
      policy:
        "User-approved claim review proposals are promoted as bounded runtime source-backed claims without deleting prior financial or narrative seeds."
    },
    totals: buildTotals(manifest.totals ?? {}, records),
    records
  };

  validateGroupManifest(updatedManifest, group);
  await writeJson(manifestPath, updatedManifest);
  promotedByGroup.push({
    groupId,
    manifestPath,
    promotedRecords: promotedRecords.length,
    firstId: promotedRecords[0]?.id ?? null,
    lastId: promotedRecords.at(-1)?.id ?? null,
    records: promotedRecords.map((record) => ({
      id: record.id,
      companyId: record.companyId,
      koreanName: record.koreanName,
      claimType: record.claimType,
      claimText: record.claimText,
      sourceManifestId: record.sourceManifestId,
      evidenceLocators: record.evidenceLocations.map((location) => `${location.markdownPath}:${location.lineNumber}`)
    }))
  });
}

const output = {
  schemaVersion: "review-approved-runtime-promotion.v0.1",
  generatedAt: now,
  inputApproval: approvalPath,
  promotionPolicy: {
    paperUseLevel: "source-backed-review-approved-claim",
    runtimeBoundary:
      "Promoted records are runtime-selectable source-backed claims, but remain bounded to the approved claim text and evidence locators.",
    overwritePolicy:
      "Existing source-backed claims are preserved. Prior records generated by this same review-approved level are replaced on rerun for idempotence.",
    customerUiBoundary:
      "Approval artifacts, candidate IDs, evidence needles, and reviewer notes remain hidden from customer UI."
  },
  totals: {
    promotedRecords: promotedByGroup.reduce((sum, group) => sum + group.promotedRecords, 0),
    byGroup: Object.fromEntries(promotedByGroup.map((group) => [group.groupId, group.promotedRecords]))
  },
  groups: promotedByGroup
};

await writeJson(outputPath, output);
await writeMarkdown(docPath, renderDoc(output));

console.log(`Review-approved runtime promotion written: ${outputPath}`);
console.log(`Readable promotion note written: ${docPath}`);
console.log(`${output.totals.promotedRecords} approved claim(s) promoted into runtime manifests.`);

async function buildPromotedRecord(record, group, sequence) {
  const company = (group.companies ?? []).find((item) => item.id === record.companyId);
  if (!company) {
    throw new Error(`${record.groupId}/${record.companyId}: companyId is missing from configs/groups.json`);
  }
  const prefix = claimPrefixByGroup[record.groupId] ?? record.groupId;
  const markdownPath = record.reviewEvidenceMarkdownPath;
  if (!markdownPath) throw new Error(`${record.candidateId}: missing reviewEvidenceMarkdownPath`);
  const markdown = await readText(markdownPath);
  const sourceTextSha256 = sha256(markdown);
  const evidenceLocations = (record.evidenceMatches ?? []).map((match) => {
    if (!match.found) throw new Error(`${record.candidateId}: cannot promote missing evidence`);
    return {
      sourceArtifact: approvalPath,
      manifestId: record.reviewSourceManifestId,
      companyId: record.companyId,
      koreanName: record.koreanName,
      title: record.reviewSourceTitle,
      sourcePageUrl: record.reviewSourcePageUrl ?? null,
      publicDocumentUrl: record.reviewPublicDocumentUrl ?? null,
      markdownPath: match.markdownPath,
      page: match.pageHeading ?? null,
      lineNumber: match.lineNumber,
      evidenceNeedle: match.needle,
      evidenceNeedleSha256: sha256(match.needle),
      sourceTextSha256,
      year: inferYear(record),
      reportingBasis: inferReportingBasis(record),
      linePreview: match.linePreview
    };
  });
  const sourceRuleIds = sourceRuleIdsFor(record);
  for (const ruleId of sourceRuleIds) {
    if (!knownPolicyRuleIds.has(ruleId)) throw new Error(`${record.candidateId}: unknown source rule ${ruleId}`);
  }
  const claimText = record.approvedClaimText;
  const officialSourceUrl = record.reviewSourcePageUrl ?? record.reviewPublicDocumentUrl ?? company.irUrl ?? null;
  const officialDownloadUrl = record.reviewPublicDocumentUrl ?? null;
  return {
    id: `${prefix}-sbc-${String(sequence).padStart(3, "0")}`,
    groupId: record.groupId,
    companyId: record.companyId,
    companyScope: "listed_company",
    koreanName: record.koreanName,
    targetWikiPage: `wiki/groups/${record.groupId}/companies/${record.companyId}.md`,
    claimType: record.claimType,
    claimText,
    claimTextSha256: sha256(claimText),
    sourceManifestId: record.reviewSourceManifestId,
    sourceTitle: record.reviewSourceTitle,
    sourceRole: "official_issuer_document",
    sourceCategory: officialSourceUrl?.includes("dart.fss.or.kr")
      ? "regulatory_filing"
      : "official_issuer_ir",
    sourceDocumentDate: inferSourceDocumentDate(record),
    sourceRuleIds,
    runtimeUsePolicy: record.runtimeUsePolicy,
    stalenessPolicy: record.stalenessPolicy,
    verificationState: "source_backed_seed",
    forwardLooking: Boolean(record.forwardLooking),
    officialSource: {
      sourcePageUrl: officialSourceUrl,
      downloadUrl: officialDownloadUrl,
      providerUrl: officialSourceUrl,
      provider: officialSourceUrl?.includes("dart.fss.or.kr") ? "DART" : "issuer IR",
      issuerIrUrl: company.irUrl ?? officialSourceUrl
    },
    evidenceNeedle: evidenceLocations[0]?.evidenceNeedle ?? null,
    evidenceLocations,
    sourceSha256: sha256(JSON.stringify(evidenceLocations)),
    sourceTextSha256,
    rightsPolicy:
      "Public source URL may be cited; extracted full text is used only for evidence location and is not redistributed.",
    reviewNote: record.reviewerRationale,
    approvalDecision: record.approvalDecision,
    reviewApprovalArtifact: approvalPath,
    paperUseLevel: "source-backed-review-approved-claim",
    promotedAt: now
  };
}

function sourceRuleIdsFor(record) {
  const ids = new Set(["SCOPE-02", "SCOPE-03", "SCOPE-04", "SRC-01", "SRC-10", "SRC-11", "SRC-12"]);
  const haystack = [
    record.claimType,
    record.runtimeUsePolicy,
    record.reviewSourcePageUrl,
    record.reviewPublicDocumentUrl,
    record.reviewSourceTitle,
    record.approvedClaimText
  ].join(" ").toLowerCase();
  if (/dart|regulatory|공시|보고서|사업보고서|분기|반기|검토보고서/u.test(haystack)) {
    ids.add("SRC-02");
    ids.add("SRC-04");
  }
  if (/ir|earnings|실적|presentation|preliminary|briefing|발표/u.test(haystack)) ids.add("SRC-05");
  if (/value|shareholder|capital|주주|환원|배당|자사주|roe|nav|pbr/u.test(haystack)) ids.add("SRC-06");
  return [...ids];
}

function validateGroupManifest(manifest, group) {
  if (manifest.schemaVersion !== "source-backed-claims.v0.1") {
    throw new Error(`${group.id}: unexpected source-backed claim schema`);
  }
  if (manifest.groupId !== group.id) throw new Error(`${group.id}: groupId mismatch`);
  if (manifest.totals.byVerificationState?.source_backed_seed !== manifest.totals.claims) {
    throw new Error(`${group.id}: all runtime claims must remain source_backed_seed`);
  }
  const ids = new Set();
  const companyIds = new Set((group.companies ?? []).map((company) => company.id));
  for (const record of manifest.records ?? []) {
    const path = `${group.id}:${record.id}`;
    if (!record.id || ids.has(record.id)) throw new Error(`${path}: missing or duplicate id`);
    ids.add(record.id);
    for (const field of [
      "groupId",
      "companyId",
      "companyScope",
      "claimType",
      "claimText",
      "claimTextSha256",
      "sourceManifestId",
      "runtimeUsePolicy",
      "verificationState"
    ]) {
      if (!record[field]) throw new Error(`${path}: missing ${field}`);
    }
    if (record.groupId !== group.id) throw new Error(`${path}: invalid groupId`);
    if (!companyIds.has(record.companyId)) throw new Error(`${path}: unknown companyId ${record.companyId}`);
    if (sha256(record.claimText) !== record.claimTextSha256) {
      throw new Error(`${path}: claimTextSha256 mismatch`);
    }
    if (!Array.isArray(record.evidenceLocations) || record.evidenceLocations.length === 0) {
      throw new Error(`${path}: missing evidenceLocations`);
    }
    for (const ruleId of record.sourceRuleIds ?? []) {
      if (!knownPolicyRuleIds.has(ruleId)) throw new Error(`${path}: unknown source rule ${ruleId}`);
    }
    if (record.paperUseLevel === "source-backed-review-approved-claim") {
      if (!record.reviewApprovalArtifact) throw new Error(`${path}: missing reviewApprovalArtifact`);
      if (!record.sourceTextSha256) throw new Error(`${path}: missing sourceTextSha256`);
      if (!record.officialSource?.sourcePageUrl && !record.officialSource?.downloadUrl) {
        throw new Error(`${path}: missing official source URL`);
      }
      for (const location of record.evidenceLocations) {
        if (!location.markdownPath || !location.lineNumber || !location.evidenceNeedleSha256) {
          throw new Error(`${path}: incomplete evidence locator`);
        }
        if (location.sourceTextSha256 !== record.sourceTextSha256) {
          throw new Error(`${path}: evidence sourceTextSha256 mismatch`);
        }
      }
    }
  }
}

function buildTotals(previousTotals, records) {
  return {
    ...previousTotals,
    claims: records.length,
    reviewApprovedClaims: records.filter(
      (record) => record.paperUseLevel === "source-backed-review-approved-claim"
    ).length,
    byCompanyId: countBy(records, (record) => record.companyId),
    byClaimType: countBy(records, (record) => record.claimType),
    byRuntimeUsePolicy: countBy(records, (record) => record.runtimeUsePolicy),
    byVerificationState: countBy(records, (record) => record.verificationState),
    byPaperUseLevel: countBy(records, (record) => record.paperUseLevel)
  };
}

function nextClaimSequence(records, prefix) {
  return (
    Math.max(
      0,
      ...records.map((record) => Number(String(record.id ?? "").match(new RegExp(`^${prefix}-sbc-(\\d+)$`, "u"))?.[1] ?? 0))
    ) + 1
  );
}

function inferSourceDocumentDate(record) {
  const value = [record.reviewSourceTitle, record.approvedClaimText].join(" ");
  const quarter = value.match(/(20\d{2}).{0,8}([1-4])\s*(?:Q|분기)/iu);
  if (quarter) return `${quarter[1]}Q${quarter[2]}`;
  const year = value.match(/20\d{2}/u);
  return year?.[0] ?? null;
}

function inferYear(record) {
  const value = inferSourceDocumentDate(record);
  return value?.match(/20\d{2}/u)?.[0] ?? null;
}

function inferReportingBasis(record) {
  return /연결/u.test(record.approvedClaimText ?? "") ? "CFS" : null;
}

function renderDoc(output) {
  const lines = [
    "# Review-Approved Runtime Claims",
    "",
    `Generated: ${output.generatedAt}`,
    "",
    "## Purpose",
    "",
    "This document records the runtime promotion of user-approved claim-review rows. The promotion preserves existing financial and narrative seeds and adds a separate `source-backed-review-approved-claim` layer.",
    "",
    "## Summary",
    "",
    `- Promoted records: ${output.totals.promotedRecords}`,
    `- Groups: ${Object.entries(output.totals.byGroup).map(([groupId, count]) => `${groupId} ${count}`).join(", ")}`,
    "",
    "## Promotion Boundary",
    "",
    "- Existing source-backed claims are preserved.",
    "- Prior review-approved runtime claims are replaced on rerun for idempotence.",
    "- Customer UI may use the promoted claim text and source links, but not approval artifacts or reviewer notes.",
    "",
    "## Promoted Rows",
    "",
    "| Group | IDs | Count | Runtime manifest |",
    "| --- | --- | ---: | --- |"
  ];
  for (const group of output.groups) {
    lines.push(
      `| ${group.groupId} | ${group.firstId} → ${group.lastId} | ${group.promotedRecords} | \`${group.manifestPath}\` |`
    );
  }
  lines.push(
    "",
    "## Approved Claim List",
    "",
    "| ID | Group | Company | Claim | Evidence locators |",
    "| --- | --- | --- | --- | --- |"
  );
  for (const group of output.groups) {
    for (const record of group.records) {
      lines.push(
        `| \`${record.id}\` | ${group.groupId} | ${record.koreanName}<br><code>${record.companyId}</code> | ${escapeMd(record.claimText)} | ${escapeMd(record.evidenceLocators.join("<br>"))} |`
      );
    }
  }
  lines.push(
    "",
    "## Source Artifacts",
    "",
    "- `raw/manifests/claim-review-approval-record.json`",
    "- `raw/manifests/review-approved-runtime-promotion.json`",
    ""
  );
  return `${lines.join("\n")}\n`;
}

async function readJson(path) {
  return JSON.parse(await readFile(join(rootDir, path), "utf8"));
}

async function readText(path) {
  return readFile(join(rootDir, path), "utf8");
}

async function writeJson(path, value) {
  const fullPath = join(rootDir, path);
  await mkdir(dirname(fullPath), { recursive: true });
  await writeFile(fullPath, `${JSON.stringify(value, null, 2)}\n`, "utf8");
}

async function writeMarkdown(path, value) {
  const fullPath = join(rootDir, path);
  await mkdir(dirname(fullPath), { recursive: true });
  await writeFile(fullPath, value, "utf8");
}

function sha256(value) {
  return createHash("sha256").update(String(value ?? "")).digest("hex");
}

function unique(values) {
  return [...new Set(values.filter(Boolean))];
}

function countBy(records, keyFn) {
  return records.reduce((acc, record) => {
    const key = keyFn(record) ?? "unknown";
    acc[key] = (acc[key] ?? 0) + 1;
    return acc;
  }, {});
}

function escapeMd(value) {
  return String(value ?? "").replace(/\|/gu, "\\|").replace(/\n/gu, "<br>");
}
